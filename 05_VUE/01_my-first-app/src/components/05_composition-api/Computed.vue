<template>
  <div>
    <h1>{{ count }}</h1>
    <button @click="increment">+</button>
  </div>
</template>

<script setup>
import { ref, computed } from 'vue';

const count = ref(0);
const double = computed(() => {
  return count.value * 2;
});

function increment() {
  count.value++;

  console.log('double: ', double);
  console.log('double.value: ', double.value);
}
</script>
